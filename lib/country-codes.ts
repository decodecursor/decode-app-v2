// Helper to find country by dial code (returns first match - use for simple lookups)
export function findCountryByCode(dialCode: string): CountryCode | undefined {
  return COUNTRY_CODES.find(c => c.code === dialCode);
}

// Helper to find country by ID (unique)
export function findCountryById(id: string): CountryCode | undefined {
  return COUNTRY_CODES.find(c => c.id === id);
}

// Get all countries sorted alphabetically (excluding duplicates from popular section)
export function getAllCountriesSorted(): CountryCode[] {
  const seen = new Set<string>();
  return COUNTRY_CODES
    .filter(c => {
      if (seen.has(c.id)) return false;
      seen.add(c.id);
      return true;
    })
    .sort((a, b) => a.country.localeCompare(b.country));
}
